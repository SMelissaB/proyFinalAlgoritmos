        // Cargar personas del archivo JSON al iniciar la aplicación
    private void cargarPersonas() {
        personas = GestionArchivos.leerPersonas();
        actualizarLista();
    }

    // Actualizar la JList con las personas
    private void actualizarLista() {
        accion=0;
        listModel.clear();
        for (Persona p : personas) {
            listModel.addElement(p.toStringComplete());
        }
    }
    
