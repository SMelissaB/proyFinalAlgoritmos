    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = jListPersonas.getSelectedIndex();
        if (index != -1) {
            personas.remove(index);
            GestionArchivos.guardarPersona(personas);
            actualizarLista();
            JOptionPane.showMessageDialog(this, "Persona eliminada con éxito.");
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una persona de la lista.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

