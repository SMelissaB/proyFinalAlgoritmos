    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        if(accion==0){ 
            int id=  personas.get(personas.size()-1).getId()+1;
            String codigo = txtCodigo.getText();
            String nombre = txtNombre.getText(); 
            String especialidad = txtEspecialidad.getText();
 
            Persona nuevaPersona = new Persona(id, codigo, nombre, especialidad); 
            personas.add(nuevaPersona);
            GestionArchivos.guardarPersona(personas); 
            actualizarLista();
            JOptionPane.showMessageDialog(this, "Persona añadida con éxito.");
 
        }else{
            persona.setCodigo(txtCodigo.getText());
            persona.setNombre(txtNombre.getText());
            persona.setEspecialidad(txtEspecialidad.getText()); 
            GestionArchivos.guardarPersona(personas);
            actualizarLista();
            JOptionPane.showMessageDialog(this, "Persona actualizada con éxito.");
        }

        limpiarForumlario();
    }//GEN-LAST:event_btnGuardarActionPerformed

    void limpiarForumlario(){
        accion = 0;
        txtCodigo.setText("");
        txtNombre.setText("");
        txtEspecialidad.setText("");
    }
