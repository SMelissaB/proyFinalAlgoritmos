    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(!formularioValido()){
            JOptionPane.showMessageDialog(this, "Hay problemas en los datos ingresados, verifique.");
        }else{
            if(accion=="N"){
                Proyecto proyectoNuevo = new Proyecto();
                if(proyectos==null){
                    this.proyectos = new ArrayList<>();
                    proyectoNuevo.setId(1);
                }else{
                    proyectoNuevo.setId(proyectos.get(proyectos.size()-1).getId()+1);
                }
                proyectoNuevo.setCodigo(txtCodigo.getText());
                proyectoNuevo.setNombre(txtNombre.getText());
                proyectoNuevo.setDescripcion(textAreaDescripcion.getText());
                //proyecto.setFechaCreacion(txtFechaCreacion.getText());
                proyectoNuevo.setHorasEstimadas(Integer.parseInt(spinHorasEstimadas.getValue().toString()));

                proyectoNuevo.setFechaCreacion(fechaActual);
                proyectoNuevo.setFechaInicio(txtFechaInicio.getText());
                proyectoNuevo.setFechaFin(txtFechaFin.getText());

                Persona personaSeleccionada = (Persona) cboResponsable.getSelectedItem();
                if (personaSeleccionada != null) {
                    int id = personaSeleccionada.getId();
                     proyectoNuevo.setResponsableId(id);
                }
                proyectoNuevo.setPorcAvance(0);
                proyectoNuevo.setEstado("No Iniciado");
                proyectoNuevo.setActivo(chckActivo.isSelected());
                proyectos.add(proyectoNuevo);
                GestionArchivos.guardarProyecto(proyectos);

                JOptionPane.showMessageDialog(this, "Proyecto añadido con éxito.");

                
            }else{// cuando accion ="E"
                Proyecto proyectoEditado = new Proyecto();
                if(proyectos==null){
                    this.proyectos = new ArrayList<>();
                    proyectoEditado.setId(1);
                }else{
                    proyectoEditado.setId(proyectoEditable.getId());
                }
                proyectoEditado.setCodigo(txtCodigo.getText());
                proyectoEditado.setNombre(txtNombre.getText());
                proyectoEditado.setDescripcion(textAreaDescripcion.getText());
                proyectoEditado.setHorasEstimadas(Integer.parseInt(spinHorasEstimadas.getValue().toString()));

                proyectoEditado.setFechaCreacion(proyectoEditable.getFechaCreacion());
                proyectoEditado.setFechaInicio(txtFechaInicio.getText());
                proyectoEditado.setFechaFin(txtFechaFin.getText());

                Persona personaSeleccionada = (Persona) cboResponsable.getSelectedItem();
                if (personaSeleccionada != null) {
                    int id = personaSeleccionada.getId();
                     proyectoEditado.setResponsableId(id);
                }
                proyectoEditado.setPorcAvance(sldAvance.getValue());
                proyectoEditado.setEstado(cboEstado.getSelectedItem().toString());
                proyectoEditado.setActivo(chckActivo.isSelected());
                for (int i = 0; i < proyectos.size(); i++) {
                    if (proyectos.get(i).getId() == proyectoEditado.getId()) {
                        proyectos.set(i, proyectoEditado);  // Reemplazamos
                        break;  // Salir del bucle una vez encontrado
                    }
                }
                        
                GestionArchivos.guardarProyecto(proyectos);

                JOptionPane.showMessageDialog(this, "Proyecto actualizado con éxito.");

                
            } 
            frmMain main = new frmMain();
            proyectos = GestionArchivos.leerProyectos();
            if(proyectos!=null){

                main.cargarProyectos();
            }
            dispose();
        } 
    }//GEN-LAST:event_btnGuardarActionPerformed

