    boolean formularioValido(){
        boolean response = true;
        if(txtCodigo.getText().isBlank()) response = false;
        if(txtNombre.getText().isBlank()) response = false;
        if(Integer.parseInt(spinHorasEstimadas.getValue().toString())<1) response = false;
        if(txtFechaInicio.getText().isBlank()) response = false;
        if(txtFechaFin.getText().isBlank()) response = false;
        if(cboResponsable.getSelectedIndex()==0) response = false;
        if(cboEstado.getSelectedIndex()==0) response = false;
        
        return response;
    }
