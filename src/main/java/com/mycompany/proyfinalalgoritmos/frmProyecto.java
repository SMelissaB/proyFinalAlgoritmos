    public frmProyecto(Proyecto proyecto){
        initComponents();
        if(proyecto ==null){
            accion ="N";
            lblTitulo.setText("NUEVO PROYECTO");
        }else{
            accion ="E";
            lblTitulo.setText("EDITAR PROYECTO");
        }
        proyectos = GestionArchivos.leerProyectos();
        personas = GestionArchivos.leerPersonas();
        
        try {
            MaskFormatter dateFormatter = new MaskFormatter("##/##/####"); 
            dateFormatter.setPlaceholderCharacter('_');
            txtFechaCreacion.setColumns(10);
            txtFechaCreacion.setFormatterFactory(new DefaultFormatterFactory(dateFormatter));
            
            txtFechaInicio.setColumns(10);
            txtFechaInicio.setFormatterFactory(new DefaultFormatterFactory(dateFormatter));
            
            txtFechaFin.setColumns(10);
            txtFechaFin.setFormatterFactory(new DefaultFormatterFactory(dateFormatter));
        }catch (ParseException e) {
            e.printStackTrace();
        }
        
        
           // Obtener el modelo actual del comboBox
        DefaultComboBoxModel<Persona> model = new DefaultComboBoxModel<>();
        model.addElement(new Persona(-1, "","(Seleccione) -"));
        for (Persona persona : personas) {
            model.addElement(persona);
        }
        cboResponsable.setModel(model); 
        
        if(accion == "N"){
            chckActivo.setSelected(true);
            fechaActual = dateFormat.format(new Date()); 
            txtFechaCreacion.setText(fechaActual);
            txtFechaCreacion.setEnabled(false);
            cboEstado.setSelectedIndex(1);
            cboEstado.setEnabled(false);
        }else{
            proyectoEditable = proyecto;
            cboEstado.setEnabled(true);
            txtCodigo.setText(proyecto.getCodigo());
            txtNombre.setText(proyecto.getNombre());
            textAreaDescripcion.setText(proyecto.getDescripcion());
            txtFechaCreacion.setText(proyecto.getFechaCreacion());
            txtFechaCreacion.setEnabled(false);
            spinHorasEstimadas.setValue(proyecto.getHorasEstimadas());
            txtFechaInicio.setText(proyecto.getFechaInicio());
            txtFechaFin.setText(proyecto.getFechaFin());
            System.out.println(proyecto.getPorcAvance());
            sldAvance.setValue(proyecto.getPorcAvance());
            
            Persona responsable = new Persona();
            for(Persona p : personas){
                if(p.getId()==proyecto.getResponsableId()){
                    cboResponsable.setSelectedItem(p);
                }
            }

            cboEstado.setSelectedItem(proyecto.getEstado());  
            chckActivo.setSelected(proyecto.isActivo());
        }

    }

